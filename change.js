const symbols = ['@', '#', '%', '&', '*', '+', '=', '$', '!', '?', '~'];
let currentIndex = 0;

function cycleSymbols() {
    const display = document.querySelector('.ascii');
    let transformedArt = asciiArt.split('').map(char => {
        return symbols.includes(char) ? symbols[(currentIndex + symbols.indexOf(char)) % symbols.length] : char;
    }).join('');
    display.innerHTML = `<pre>${transformedArt}</pre>`;
    currentIndex = (currentIndex + 1) % symbols.length;
}

setInterval(cycleSymbols, 100); // Change every 500ms
